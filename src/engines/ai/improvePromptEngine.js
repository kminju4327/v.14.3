// ============================================================
// AI 섹션 개선 프롬프트 엔진 (수정판)
// ============================================================
// 사용자 instruction을 실제로 반영하는 텍스트 프롬프트로 변환
// ============================================================

export function buildImprovePrompt({
  section = {},
  productInfo = {},
  category = "",
  targetCustomer = "",
  brainKnowledge = {},
  tone = "",
  instruction = ""
}) {

  // 프롬프트 구조를 텍스트로 변환
  const sectionTitle = section.title || "제목 없음";
  const sectionBody = section.body || "본문 없음";
  const sectionItems = Array.isArray(section.items) ? section.items : [];
  
  const productName = productInfo.name || "제품";
  const productBenefits = productInfo.benefits || "건강 관리";
  const productCategory = category || "일반 식품";
  
  // ✅ 수정: SectionEditor에서 전달된 instruction을 재분류하지 않고 그대로 사용
  // determineInstructionType()와 buildInstructionBlock() 호출 제거
  
  const textPrompt = `
당신은 이커머스 제품 상세페이지의 섹션을 개선하는 전문 카피라이터입니다.

=====================================================
[현재 섹션 정보]
=====================================================

제목: ${sectionTitle}

본문:
${sectionBody}

핵심 포인트:
${sectionItems.length > 0 ? sectionItems.map((item, i) => `${i + 1}. ${item}`).join("\n") : "없음"}

=====================================================
[제품 & 카테고리 정보]
=====================================================

제품명: ${productName}
제품 설명: ${productBenefits}
카테고리: ${productCategory}
타겟 고객: ${targetCustomer || "기본"}
제품 톤: ${tone || "신뢰감 있는"}

=====================================================
[최우선 사용자 개선 요청]
=====================================================

${instruction || "기존 내용을 더 명확하게 개선하세요."}

중요:
위 사용자 요청과 아래 공통 안전 규칙이 충돌하면
더 엄격한 금지 규칙을 우선합니다.

=====================================================
[공통 안전 규칙]
=====================================================

- 제품 정보와 현재 섹션 내용에 존재하는 사실만 사용
- 확인되지 않은 인증(GMP, HACCP 등), 연구, 논문, 임상, 특허, 수치 생성 금지
- 확인되지 않은 고객 후기, 판매량, 만족도 생성 금지
- 제공되지 않은 효능과 효과 생성 금지
- 일반식품은 질환 개선, 혈당 개선, 체중 감소 등 기능성 표현 생성 금지
- 정보가 없으면 추측하지 않고 생략
- 현재 섹션의 제목, 본문, 핵심 포인트 구조 유지
- 반드시 JSON 형식으로 응답

=====================================================
[개선 규칙]
=====================================================

1. 기존 의도 유지
   - 제품의 핵심 메시지를 바꾸지 마세요
   - 설득 논리의 순서는 유지하세요

2. 요청 방향 100% 반영
   - 사용자의 개선 요청을 명확히 따르세요
   - 요청이 애매하면 보수적으로 해석하세요

3. 한국어 품질
   - 자연스러운 한국어 사용
   - 문법 오류 없음
   - 조사, 띄어쓰기 완벽

4. 길이 관리
   - 제목: 한 줄 (10-15글자)
   - 본문: 2-3 문단 (70-150글자)
   - 핵심 포인트: 3-5개

5. 구조 유지
   - 기존 단락 구조 참고
   - 리스트 형식 유지 (있으면)

=====================================================
[출력 형식]
=====================================================

반드시 JSON 형식으로만 응답하세요:

{
  "title": "개선된 제목",
  "body": "개선된 본문 (여러 문단 가능)",
  "items": ["항목1", "항목2", "항목3"],
  "improvement_reason": "이렇게 개선한 이유"
}

주의:
- 다른 텍스트는 절대 추가하지 말 것
- 따옴표, 줄바꿈 등 JSON 규칙 준수
- body는 '\\n'으로 문단 구분
`;


  return textPrompt;
}
// ✅ 수정: determineInstructionType와 buildInstructionBlock은 사용되지 않으므로 삭제됨
// buildImprovePrompt에서는 SectionEditor의 instruction을 그대로 사용합니다.
