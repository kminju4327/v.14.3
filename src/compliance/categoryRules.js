// 카테고리 규칙 조회. 없는 카테고리는 안전하게 '일반식품' 규칙으로 폴백한다.
export function getCategoryRules(category) {
  return CATEGORY_RULES[category] || CATEGORY_RULES["일반식품"];
}

// 생성 프롬프트에 삽입할 카테고리 제약 문자열을 조립한다.
export function buildGenerationConstraint(category) {
  const rules = getCategoryRules(category);
  const extra = rules.extraRules ? "\n- " + rules.extraRules.join("\n- ") : "";
  return (
    `${rules.generationGuidance}
` +
    `- 절대 사용 금지 단어/맥락: ${rules.forbiddenWords.join(", ")}
` +
    `- "~걱정", "~불안", "~증상" 같은 표현도 특정 질환/건강 이상을 암시하면 안 됨
` +
    `- 타사 제품을 직접 비교·비하하는 뉘앙스 금지, 자사 제품 사실만 서술
` +
    `- 원료 함량 수치(%)는 순도인지 최종 섭취량 기준 함량인지 모호하지 않게, 확실하지 않으면 "정확한 함량은 성분표를 참조하세요" 수준으로만 언급` +
    extra
  );
}

// 컴플라이언스 체크 프롬프트에 삽입할 '카테고리별 주의 항목' 문자열을 조립한다.
export function buildComplianceFocus(category) {
  const rules = getCategoryRules(category);
  return rules.complianceFocus.map((f) => "- " + f).join("\n");
}
