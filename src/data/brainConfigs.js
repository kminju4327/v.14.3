// Brain 선택 함수 (핵심: 건강관리 식품만 subCategory로 매핑)
// ============================================================
export function getBrain(mainCategory, subCategory, productName = "") {
  const main = String(mainCategory || "").trim();
  const sub = String(subCategory || "").trim();
  const name = String(productName || "").trim();

  // 건강·영양식품은 사용자가 선택한 법적 분류를 최우선으로 사용한다.
  if (["건강·영양식품", "건강기능식품", "건강관리 식품"].includes(main)) {
    if (sub === "건강기능식품") return HEALTH_FUNCTIONAL_FOOD_BRAIN_V6;
    if (sub === "일반 건강식품") return GENERAL_HEALTH_FOOD_BRAIN_V6;
    if (sub === "단백질·영양식" || sub === "단백질 식품") {
      return BRAIN_CONFIGS["건강관리 식품|단백질 식품"] || GENERAL_HEALTH_FOOD_BRAIN_V6;
    }
    if (sub === "체중조절용 식품" || sub === "다이어트 식품") {
      return BRAIN_CONFIGS["건강관리 식품|다이어트 식품"] || GENERAL_HEALTH_FOOD_BRAIN_V6;
    }
    return GENERAL_HEALTH_FOOD_BRAIN_V6;
  }

  // 즉석밥 계열만 Rice Brain으로 보낸다. 라면은 일반 간편식 Brain으로 처리한다.
  if (["즉석밥", "죽"].includes(sub) || /햇반|즉석밥|컵밥|간편밥/i.test(name)) {
    return BRAIN_CONFIGS["식품"] || null;
  }

  return BRAIN_CONFIGS[main] || null;
}
